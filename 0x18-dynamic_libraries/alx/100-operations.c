#include <stdio.h>

/**
 * add - add two long integers
 * @a: first int
 * @b: second int
 *
 * Return: a + b
 */
long add(long a, long b)
{
	return (a + b);
}

/**
 * sub - subtract two long integers
 * @a: first int
 * @b: second int
 *
 * Return: a - b
 */
long sub(long a, long b)
{
	return (a - b);
}

/**
 * mul - multiply two long integers
 * @a: first int
 * @b: second int
 *
 * Return: a * b
 */
long mul(long a, long b)
{
	return (a * b);
}

/**
 * div - divide two long integers
 * @a: first int
 * @b: second int
 *
 * Return: a / b
 */
long div(long a, long b)
{
	return (a / b);
}

/**
 * mod - find remainder
 * @a: first int
 * @b: second int
 *
 * Return: a % b
 */
long mod(long a, long b)
{
	return (a % b);
}
